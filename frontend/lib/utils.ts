export const getRoomId = (uid1: string, uid2: string) => {
  return [uid1, uid2].sort().join("-");
};

export const formatDate = (date: Date) => {
  let day = date.getDate();
  let months = [
    "Jan",
    "Feb",
    "Mar",
    "Apr",
    "May",
    "Jun",
    "Jul",
    "Aug",
    "Sep",
    "Oct",
    "Nov",
    "Dec",
  ];
  let month = months[date.getMonth()];
  return `${day} ${month}`;
};
